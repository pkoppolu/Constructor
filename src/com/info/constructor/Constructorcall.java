package com.info.constructor;
public class Constructorcall {
	Constructorcall(int x){
			System.out.println("Printing Integer number: "+x);
	}
	Constructorcall(char x){
		System.out.println("Printing Character: "+x);
    }
	Constructorcall(String x){
		System.out.println("Printing String name: "+x);
    }
	Constructorcall(double x){
		System.out.println("Printing double number: "+x);
    }
}
